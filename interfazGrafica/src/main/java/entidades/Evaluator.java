package entidades;

/**
 *
 * @author Unicauca
 */
public class Evaluator {
    private Long id;
    private String name;

    public Evaluator(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name; // Mostrar el nombre en el JComboBox
    }
}
